import Foundation

let c = readLine()!
switch c {
    case "A+":
        print(4.3)
    case "A0":
        print(4.0)
    case "A-":
        print(3.7)
    case "B+":
        print(3.3)
    case "B0":
        print(3.0)
    case "B-":
        print(2.7)
    case "C+":
        print(2.3)
    case "C0":
        print(2.0)
    case "C-":
        print(1.7)
    case "D+":
        print(1.3)
    case "D0":
        print(1.0)
    case "D-":
        print(0.7)
    case "F":
        print(0.0)
    default:
        print("")
}
